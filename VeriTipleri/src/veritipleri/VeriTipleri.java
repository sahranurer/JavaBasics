package veritipleri;


public class VeriTipleri {

    public static void main(String[] args) {
        long a1=160;
        int a2 = 150;
        short a3 = 140;
        byte a4 = 120;
        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);
        System.out.println("Byte degeri = " +a4);
        
        double b1= 3.14;
        float b2=2.11f;
        System.out.println("double degeri = " +b1);
        System.out.println("float degeri = "+b2);
        
        char c1 = 'a';
        boolean c2 = true;
        System.out.println("char degeri = " +c1);
        System.out.println("boolean degeri = " +c2);
    }
    
}
